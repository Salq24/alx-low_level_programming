#include "3-calc.h"

/**
 * op_add - returns the sum of 2 integers
 * @a: int 1
 * @b: int 2
 * Return: sum
 */
int op_add(int a, int b)

{
	return (a + b);
}

/**
 * op_sub - return the diff btw 2 ints
 * @a: int1
 * @b: int 2
 * Return: diff
 */

int op_sub(int a, int b)

{
	return (a - b);
}

/**
 * op_mul - return the product of 2 int
 * @a: int1
 * @b: int 2
 * Return: product
 */
int op_mul(int a, int b)

{
	return (a * b);
}

/**
 * op_div - return result of division
 * @a: int1
 * @b: int 2
 * Return: division result
 */

int op_div(int a, int b)

{
	return (a / b);
}

/**
 * op_mod - returns the remainder of
 * the division of a by b
 * @a: int 1
 * @b: int2
 * Return: result
 */

int op_mod(int a, int b)

{
	return (a % b);
}

